
namespace OpenSvg.GeoJson;

/// <summary>
/// Contains names for commonly used GeoJson attributes.
/// </summary>
public static class GeoJsonNames
{
    /// <summary>
    /// The stroke color attribute.
    /// </summary>
    public const string Stroke = "stroke";

    /// <summary>
    /// The stroke width attribute.
    /// </summary>
    public const string StrokeWidth = "stroke-width";

    /// <summary>
    /// The fill color attribute.
    /// </summary>
    public const string Fill = "fill";

    /// <summary>
    /// The fill opacity attribute.
    /// </summary>
    public const string FillOpacity = "fill-opacity";

    /// <summary>
    /// The text content attribute.
    /// </summary>
    public const string Text = "text";

    /// <summary>
    /// The font name attribute.
    /// </summary>
    public const string FontName = "font-name";

    /// <summary>
    /// The font size attribute.
    /// </summary>
    public const string FontSize = "font-size";

    /// <summary>
    /// The text color attribute .
    /// </summary>
    public const string TextColor = "text-color";
}